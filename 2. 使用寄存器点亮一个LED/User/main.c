#include "stm32f10x.h"

typedef unsigned int u32;   //类型重定义 unsigned int -- u32

void SystemInit()
{
	
}

/*******************************************************************************
* 函 数 名         : delay
* 函数功能		   : 延时函数，通过while循环占用CPU，达到延时功能
* 输    入         : i
* 输    出         : 无
*******************************************************************************/
void delay(u32 i)
{
	while(i--);
}

/*******************************************************************************
* 函 数 名         : main
* 函数功能		   : 主函数
* 输    入         : 无
* 输    出         : 无
*******************************************************************************/
int main()
{
	RCC_APB2ENR |= 1<<4;
	GPIOC_CRL &= ~( 0x0F<< (4*0));
	GPIOC_CRL |= (3<<4*0);
	GPIOC_BSRR=(1<<(16+0));
	while(1)
	{
		GPIOC_BSRR=(1<<(16+0));
		delay(0xFFFFF);
		GPIOC_BSRR=(1<<(0));
		delay(0xFFFFF);
	}
}



